    private void txtProcurarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProcurarKeyReleased
        
        
     
        ResultSet rs = ob.listarAlunos();
        
        String cs = cbProcurar.getSelectedItem().toString();
        
        if(cs.equals("Nome"))
            rs = ob.buscarAlunoPorNome(txtProcurar.getText());
        else if(cs.equals("Cidade"))
            rs = ob.buscarAlunoPorCidade(txtProcurar.getText());
    
        
        carregarTabela(rs);
    }//GEN-LAST:event_txtProcurarKeyReleased

