    private void carregarTabela(ResultSet rs) {
            
        String[] colunasTabela = new String[]{"Nome","Endereço","Telefone","Curso","InstEnsino","Cidade","Bairro"};
        
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela){
            
            public boolean isCellEditable(int row, int col){
                return false;
            }
        };
        
        tbAlunos.setModel(modeloTabela);
        tbAlunos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        
        int vColIndex = 0;
        TableColumn col = tbAlunos.getColumnModel().getColumn(vColIndex);
        int width = 200;
        col.setPreferredWidth(width);

        try{
            rs.previous();
            while(rs.next()){
                modeloTabela.addRow(new String[]{
                    rs.getString(1),rs.getString(2),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9),rs.getString(3)
                    });                
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
